					<h3 >Симптомы</h3>
					<p>Инкубационный период, то есть время, прошедшее от момента заражения до появления первых признаков болезни, составляет от 1 до 14 дней. Первые симптомы при заражении человеком человека могут уже появлятся через 5 дней, а если передача вируса произошла от животного, то инкубационный период продлится дольше. 			Коронавирус может передаваться на этапе инкубационного периода, при этом заболевший не имеет внешних проявлений болезни. Это услугубляет ситуацию с распространением вируса. Так же заболевание может проходить безсимптомно, но в общем случае симптомы такие же, как в случае с ОРВИ:</p>
					<ul>
						<li>высокая температура(38°С или выше, озноб)</li>
						<li>одышка, затруднения при дыхании</li>
						<li>сухой, непродуктивный кашель</li>
						<li>боли в области груди</li>
						<li>головная боль</li>
						<li>мышечная боль</li>
						<li>общее недомогание</li>
						<li>потеря сознания</li>
					</ul>
					<p>Реже такие же, как симптомы в случае пневмонии.</p>
