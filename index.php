					<h3 >Коронавирус (2019-nCoV)</h3>
					<p>
					Коронавирус(2019-nCoV) или уханьский коронавирус - новый штам коронавируса. Впервые был зарегистрирован В Ухане во время вспышки пневнонии.</p>
					<p>
					В зону риска попадают люди с ослабленным иммунитетом. Чаще всего пожилые люди и дети.
					</p>
					<p>Коронавирус передается воздушно-капельным путем при тесном контакте с носителем вируса, так и контактном способе - через прикосновения и пожатия.</p>
